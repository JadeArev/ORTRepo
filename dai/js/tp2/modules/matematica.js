//4 FUNCIONES

let pi = 3.14532;

const Sumar = (x,y) => {

    return x+y;
}

const Restar = (x,y) => {

return x-y;

}

const Multiplicar = (x,y) => {

    return x*y;
}

const Dividir = (x,y) => {

    return x/y;
}

const CalPi = (x) => {

    return x *pi;
}

module.exports={Restar,Sumar,Multiplicar,Dividir,CalPi}